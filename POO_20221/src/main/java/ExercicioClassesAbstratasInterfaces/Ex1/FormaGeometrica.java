package ExercicioClassesAbstratasInterfaces.Ex1;

public class FormaGeometrica {
    double area;
    double perimetro;
    
    void setArea(double area){
        this.area = area;
    }
    
    void setPerimetro(double perimetro){
        this.perimetro = perimetro;
    }
    
    double getArea(){
        return this.area;
    }
    
    double getPerimetro(){
        return this.perimetro;
    }
}